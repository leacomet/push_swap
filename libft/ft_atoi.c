int	ft_atoi(const char *nptr)
{
	int	nb;
	int	i;
	int	neg;

	nb = 0;
	i = 0;
	neg = 1;
	while ((nptr[i] > 8 && nptr[i] < 14) || nptr[i] == 32)
		i++;
	if ((nptr[i] == '-' || nptr[i] == '+')
		&& (nptr[i + 1] > 47 && nptr[i + 1] < 58))
	{
		if (nptr[i] == '-')
			neg *= -1;
		i++;
	}
	while ((nptr[i] > 47 && nptr[i] < 58))
	{
		nb *= 10;
		nb += (nptr[i] - '0');
		i++;
	}
	return (nb * neg);
}
