#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	temp;

	if (n == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (n < 0)
		{
			n = -n;
			write(fd, "-", 1);
		}
		temp = n % 10 + 48;
		if (n > 9)
			ft_putnbr_fd(n / 10, fd);
		write(fd, &temp, 1);
	}
}
